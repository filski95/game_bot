import pyautogui
import time


class Fishing:
    def __init__(self, fishing_spots) -> None:
        self.fishing_rod = self.find_fishing_rod()
        self.fishing_done_at = None
        self.fish_spots = fishing_spots

    def find_fishing_rod(self):
        fs = pyautogui.locateOnScreen("ss/fishing_rod.png", confidence=0.8)

        return fs

    def use_fishing_rod(self, should_run):
        print("fishing...")
        # x, y = self.get_spot_to_fish()
        for cords in self.fish_spots:
            x, y = cords
            if should_run["run"] is False:
                return False

            time.sleep(0.2)
            pyautogui.moveTo(self.fishing_rod)
            time.sleep(0.10)
            pyautogui.click(button="right")
            time.sleep(0.15)
            pyautogui.moveTo(x, y)
            time.sleep(0.15)
            pyautogui.click(button="left")
        return True


